print("Hello, World!!!!")

#Quando quiser guardar uma String (Frase) 
nome = "Lucas Lima Lopes"
#Quando quiser guardar um número inteiro
idade = 20

#Exibir o nome (que esta dentro da variavel nome)
print(nome)
#Maneiras para quando quiser exibir a frase "Minha idade é" completando com o conteúdo da variável "idade"
print("A minha idade é "+str(idade))

print(f"A minha idade é {idade}")

print("A minha idade é {}".format(idade))

#Quando quiser exibir "Meu nome é... e tenho... anos" trocando pelas variáveis "nome" e "idade"
print("Meu nome é {} e tenho {} anos".format(nome,idade))

